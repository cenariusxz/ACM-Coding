#include<stdio.h>
#include<string.h>
#include<vector>
using namespace std;
int a[1000]={1000000,111111,111110,111101,111100,111011,111010,111001,111000,110111,110110,110101,110100,110011,110010,110001,110000,101111,101110,101101,101100,101011,101010,101001,101000,100111,100110,100101,100100,100011,100010,100001,100000,11111,11110,11101,11100,11011,11010,11001,11000,10111,10110,10101,10100,10011,10010,10001,10000,1111,1110,1101,1100,1011,1010,1001,1000,111,110,101,100,11,10,1};
char aa[10];

int main(){
	int n,ans=0,i;
	vector<int>b;
	b.clear();
	scanf("%s",aa);
	int l=strlen(aa);
	for(i=0;i<l;i++){
		if(aa[i]=='0')continue;
		int j,tmp=0,t=1;
		ans++;
		for(j=l-1;j>=i;j--){
			if(aa[j]>'0'){
				tmp+=t;
				aa[j]--;
			}
			t*=10;
		}
		b.push_back(tmp);
		if(aa[i]>'0')i--;
	}
	printf("%d\n",ans);
	for(i=b.size()-1;i>=0;i--){
		printf("%d",b[i]);
		if(i==0)printf("\n");
		else printf(" ");
	}
	return 0;
}
